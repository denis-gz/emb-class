/* USER CODE BEGIN 4 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    // Check if the interrupt came from PA0
    if (GPIO_Pin != ENC_KEY_Pin)
    	return;

    int64_t now_time = stm_timer_get_time();
    GPIO_PinState key_state = HAL_GPIO_ReadPin(ENC_KEY_GPIO_Port, ENC_KEY_Pin);

    // Check if pin level is unchanged
    if (g_key_state == key_state) {
      if (key_state == GPIO_PIN_SET)    // High level - button remains released
        g_depress_time = 0;
      else
        g_release_time = 0;	            // Low level - button remains depressed
    }
    else {                              // Level changed
      if (key_state == GPIO_PIN_SET)
        g_release_time = now_time;
      else
        g_depress_time = now_time;
    }

    g_key_state = key_state;            // Store actual values

    if (g_key_state == GPIO_PIN_RESET
        && g_depress_time               // Exclude consecutive depress or release events
        && g_release_time
        && g_depress_time - g_release_time > HIGH_STATE_GUARD_INTERVAL_US) {

    	SendUartCommand(ENC_KEY);
    }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// Check which timer triggered the interrupt
	if (htim->Instance == TIM3) {
		// Check the Direction Bit (DIR) in the Control Register 1 (CR1)
		// 0 = Counting Up (CW)
		// 1 = Counting Down (CCW)
		if (__HAL_TIM_IS_TIM_COUNTING_DOWN(htim)) {
			// DIRECTION: CCW (Counter-Clockwise)
			// User rotated Left
    	SendUartCommand(ENC_CCW);
		}
		else {
			// DIRECTION: CWS (Clockwise)
			// User rotated Right
    	SendUartCommand(ENC_CWS);
		}
	}
}

void SendUartCommand(const char* command)
{
	HAL_UART_Transmit(&huart1, (uint8_t*) command, strlen(command), 1);
}
